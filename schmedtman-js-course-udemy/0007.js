// Initialize a variable 'x' with the sum of 16 and 34.
// Add 12 to 'x' using the addition assignment operator.
// Multiply 'x' by 4 using the multiplication assignment operator.
// Increment 'x' by 1.
// Decrement 'x' by 1 twice.
// Add comments to explain each step.
// Print the final value of 'x' to the console.


let x = 16 + 34; // 50

x += 12; // addition assignment operator
// x = 50 + 12 = 62

x *= 4; // multiplication assignment operator
// x = 62 * 4 = 248

x++; // increment by 1
// x = 248 + 1 = 249

x--;
x--; // decrement by 1
// x = 249 - 2 = 247

console.log(x); //247